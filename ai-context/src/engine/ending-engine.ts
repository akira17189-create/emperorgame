/**
 * 检查是否触发了任何结局
 * @param state 游戏状态
 * @returns 触发的结局，如果没有则返回null
 */
export function checkEndings(state: GameState): Ending | null {
  // 按优先级降序检查
  const sortedEndings = [...ENDINGS].sort((a, b) => b.priority - a.priority);

  for (const ending of sortedEndings) {
    if (ending.trigger(state)) {
      return ending;
    }
  }

  return null;
}

/**
 * 渲染结局叙事，替换占位符为实际游戏事件
 * @param ending 结局对象
 * @param state 游戏状态
 * @returns 渲染后的叙事文本
 */
export function renderEndingNarrative(ending: Ending, state: GameState): string {
  let narrative = ending.narrative;

  // 获取集体记忆中的事件
  const collectiveMemory = state.world.collective_memory || [];

  // 替换占位符
  narrative = narrative.replace(/\{\{event_1\}\}/g, () => {
    if (collectiveMemory.length > 0) {
      return collectiveMemory[0] || '登基之初';
    }
    return '登基之初';
  });

  narrative = narrative.replace(/\{\{event_2\}\}/g, () => {
    if (collectiveMemory.length > 1) {
      return collectiveMemory[1] || '新政推行';
    }
    return '新政推行';
  });

  return narrative;
}
